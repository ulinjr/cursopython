print("Convertidor de grados Centígrados a grados Farenheit")
grados_celsius = float(input("Ingresa la temperatura en grados celsius ")) 
grados_farenheit = (grados_celsius * 1.8) + 32
print(f"La temperatura en grados Farenheit es de :",grados_farenheit)